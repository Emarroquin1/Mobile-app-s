package com.example.proyectofinalmobiles;

public class Experiencia {
    private int punto;
    private String apodo;

    public Experiencia(int punto, String apodo) {
        this.punto = punto;
        this.apodo = apodo;
    }

    public int getPunto() {
        return punto;
    }

    public String getApodo() {
        return apodo;
    }
}
